class Solution:
    def depthSum(self, nestedList: List[NestedInteger]) -> int:
        #identify depth for multiplier based on level
        #identify if isinstance(item, string/int)
        #time complexity O(n)
        #space complexity O(N)
        return recursive_calculate_int_depth(nestedList)

def recursive_calculate_int_depth(array, total=0, depth=0):
    depth += 1
    for index in range(len(array)):
        if array[index].isInteger():
            total += array[index].getInteger() * depth
        else:
            total = recursive_calculate_int_depth(array[index].getList(), total, depth)
    return total
        
    
                
        